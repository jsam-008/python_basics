# this are the data types in python 


#ext Type:	str
#Numeric Types:	int, float, complex
#Sequence Types:	list, tuple, range
#Mapping Type:	dict
#Set Types:	set, frozenset
#Boolean Type:	bool
#Binary Types:	bytes, bytearray, memoryview
#None Type:	NoneType


# Text Type
text = " This is the data type of python"

# Numeric Types
integer_num = 10
float_num = 10.5
complex_num = 3 + 4j

# Sequence Types
my_list = [1, 2, 3, 4]
my_tuple = (5, 6, 7, 8)
my_range = range(5)

# Mapping Type
my_dict = {"name": "Sam", "age": 19}

# Set Types
my_set = {1, 2, 3}
my_frozenset = frozenset([4, 5, 6])

# Boolean Type
is_python_fun = True

# Binary Types
binary_data = b"Python"
byte_array = bytearray(5)
memory_view_data = memoryview(byte_array)

# None Type
nothing = None

# Printing all data types
print("Text Type:", text)
print("Integer:", integer_num)
print("Float:", float_num)
print("Complex:", complex_num)
print("List:", my_list)
print("Tuple:", my_tuple)
print("Range:", list(my_range)) 
print("Dictionary:", my_dict)
print("Set:", my_set)
print("Frozen Set:", my_frozenset)
print("Boolean:", is_python_fun)
print("Bytes:", binary_data)
print("Byte Array:", byte_array)
print("Memory View:", memory_view_data)
print("None Type:", nothing)

